package expedition3gpp

import (
	"fmt"
)

// --------------------------------------------------
// Output
// --------------------------------------------------
func formatOutput(spec []Specification) {
	fmt.Println("+-----+---------+----------------------------------------------------------------------------------+")
	fmt.Println("| No. | Version | URL                                                                              |")
	fmt.Println("+-----+---------+----------------------------------------------------------------------------------+")
	for i := 0; i < len(spec); i++ {
		fmt.Printf("| %3d | %7s | %-80s |\n", i + 1, spec[i].version, spec[i].url)
	}
	fmt.Println("+-----+---------+----------------------------------------------------------------------------------+")
}
